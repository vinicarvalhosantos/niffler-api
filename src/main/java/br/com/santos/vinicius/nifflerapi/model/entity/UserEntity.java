package br.com.santos.vinicius.nifflerapi.model.entity;

import br.com.santos.vinicius.nifflerapi.model.TwitchUserModelData;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBAttribute;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBAutoGeneratedKey;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBHashKey;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBTable;
import lombok.Getter;
import lombok.NoArgsConstructor;
import lombok.Setter;

import java.io.Serializable;
import java.util.Date;
import java.util.Objects;
import java.util.UUID;

@Getter
@Setter
@NoArgsConstructor
@DynamoDBTable(tableName = "user")
public class UserEntity implements Serializable {

    private static final long serialVersionUID = 8910812764568406489L;

    @DynamoDBHashKey
    @DynamoDBAutoGeneratedKey
    private String id = UUID.randomUUID().toString();

    @DynamoDBAttribute(attributeName = "user_id")
    private Long userId;

    @DynamoDBAttribute(attributeName = "username")
    private String username;

    @DynamoDBAttribute(attributeName = "display_name")
    private String displayName;

    @DynamoDBAttribute(attributeName = "points_to_add")
    private Double pointsToAdd = 0.0;

    @DynamoDBAttribute(attributeName = "points_added")
    private Double pointsAdded = 0.0;

    @DynamoDBAttribute(attributeName = "created_at")
    private Date createdAt = new Date();

    public UserEntity(Long userId, String username, String displayName, Double pointsToAdd, Double pointsAdded) {
        this.userId = userId;
        this.username = username;
        this.displayName = displayName;
        this.pointsToAdd = pointsToAdd;
        this.pointsAdded = pointsAdded;
        this.createdAt = new Date();
    }

    public boolean equalsTwitchUser(Object obj) {
        if (obj == null) return false;
        if (obj == this) return true;
        return Objects.equals(this.username, ((TwitchUserModelData) obj).getLogin())
                && Objects.equals(this.displayName, ((TwitchUserModelData) obj).getDisplay_name());
    }
}
